function [p, pu, puu] = cubicSplineDers(l, C)
% 利用拟合的三次多项式计算拟合五次多项式需要的参数
% l为此段长度，即参数u在区间[0, l]中，C为三次样条曲线的系数
p = zeros(2, 3);
pu = zeros(2, 3);
puu = zeros(2, 3);

p(1, :) = C(4, :);
p(2, :) = C(1, :) * l^3 + C(2, :) * l^2 + C(3, :) * l + C(4, :);

% 求切矢，并进行单位化
pu(1, :) = C(3, :);
pu(1, :) = pu(1, :) / norm(pu);
pu(2, :) = 3 * C(1, :) * l^2 + 2 * C(2, :) * l + C(3, :);
pu(2, :) = pu(2, :) / norm(pu(2, :));

puu(1, :) = 2 * C(2, :);
puu(2, :) = 6 * C(1, :) * l + 2 * C(2, :);
